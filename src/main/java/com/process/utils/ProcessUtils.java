package com.process.utils;

public class ProcessUtils {
	
	public static Integer computeSum(Integer a, Integer b) {
		return a+b;
	}
	
	public static Integer computeDifference(Integer a, Integer b) {
		return a-b;
	}
	
	public static Integer computeProduct(Integer a, Integer b) {
		return a*b;
	}
	
	public static Integer computeQuotient(Integer a, Integer b) {
		return a/b;
	}

}
